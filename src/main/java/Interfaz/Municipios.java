    private void editarMunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarMunActionPerformed
        /*Si no hay ninguna fila seleccionada, mostrará un mensaje de error*/
        if (listadeMunicipios.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            /*Si alguna fila fue seleccionada, procede a enviar los datos de la fila a los TextFields
            para poder editarlos.*/
            filaSeleccionadaMun = listadeMunicipios.getSelectedRow();
            códigoDepartamentoMun.setText(listadeMunicipios.getValueAt(listadeMunicipios.getSelectedRow(), 0).toString());
            nombreMunicipio.setText(listadeMunicipios.getValueAt(listadeMunicipios.getSelectedRow(), 1).toString());
        }
    }//GEN-LAST:event_editarMunActionPerformed

