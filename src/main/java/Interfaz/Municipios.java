    private void agregarMunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarMunActionPerformed
        boolean encontrado = false;
        for (int i = 0; i < listaDepartamentos.size(); i++) {
            if (códigoDepartamentoMun.getText().equals(listaDepartamentos.get(i).getCodDpto())) {
                encontrado = true;
                break;
            }
        }
        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "Este departamento no se encuentra disponble", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        if (códigoDepartamentoMun.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese el código del Departamento", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        if (nombreMunicipio.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del Municipio", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        Municipio mun = new Municipio(códigoDepartamentoMun.getText(), nombreMunicipio.getText());
        listaMunicipios.add(mun);
        model.addRow(new Object[]{códigoDepartamentoMun.getText(), nombreMunicipio.getText()});
        listadeMunicipios.setModel(model);
        JOptionPane.showMessageDialog(this, "Municipio agregado correctamente", "Proceso Exitoso", JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(true);
        códigoDepartamentoMun.setText("");
        nombreMunicipio.setText("");
    }//GEN-LAST:event_agregarMunActionPerformed

    private void modificarMunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarMunActionPerformed
        Municipio municipioSeleccionado = listaMunicipios.get(filaSeleccionadaMun);
        //Colocar el nuevo municipio modificado en la tabla y en el ArrayList.
        municipioSeleccionado.setCodDptoMun(códigoDepartamentoMun.getText().trim());
        municipioSeleccionado.setNombreMun(nombreMunicipio.getText().trim());
        //Actualizar los datos en la tabla.
        listadeMunicipios.setValueAt(códigoDepartamentoMun.getText().trim(), filaSeleccionadaMun, 0);
        listadeMunicipios.setValueAt(nombreMunicipio.getText().trim(), filaSeleccionadaMun, 1);
        JOptionPane.showMessageDialog(null, "Kiosco modificado correctamente", "Proceso exitoso", JOptionPane.INFORMATION_MESSAGE);
        //Limpiando los TextFields.
        códigoDepartamentoMun.setText("");
        nombreMunicipio.setText("");
    }//GEN-LAST:event_modificarMunActionPerformed

