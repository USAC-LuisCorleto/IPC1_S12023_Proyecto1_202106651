    private void agregarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarTarjetaActionPerformed
        if (nombreTarjeta.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe agregar el nombre del propietario de la tarjeta", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        if (numTarjeta.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe agregar el número de la tarjeta", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        if (!numTarjeta.getText().matches(".*[0-9].*") || numTarjeta.getText().matches(".*[A-Z].*") || numTarjeta.getText().matches(".*[a-z].*")) {
            JOptionPane.showMessageDialog(this, "Solo debe ingresar números", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        if (fechaVencimiento.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe agregar la fecha de vencimiento", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        Tarjeta tar = new Tarjeta(nombreTarjeta.getText(), numTarjeta.getText(), fechaVencimiento.getText());
        listaTarjetas.add(tar);
        String numTarjetaCompleto = numTarjeta.getText();
        String numTarjetaOculto = "**************" + numTarjetaCompleto.substring(14);
        model.addRow(new Object[]{nombreTarjeta.getText(), numTarjetaOculto, fechaVencimiento.getText()});
        listadeTarjetas.setModel(model);
        JOptionPane.showMessageDialog(this, "Tarjeta agregada correctamente", "Proceso Exitoso", JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(true);
        nombreTarjeta.setText("");
        numTarjeta.setText("");
        fechaVencimiento.setText("");
    }//GEN-LAST:event_agregarTarjetaActionPerformed

