    private void editarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarTarjetaActionPerformed
        //Verificando si alguna fila no fue seleccionada.
        if (listadeTarjetas.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            /*Si alguna fila fue seleccionada, procede a enviar los datos de la fila a los TextFields
            para poder editarlos.*/
            filaSeleccionadaTarjetas = listadeTarjetas.getSelectedRow();
            nombreTarjeta.setText(listadeTarjetas.getValueAt(listadeTarjetas.getSelectedRow(), 0).toString());
            numTarjeta.setText(listadeTarjetas.getValueAt(listadeTarjetas.getSelectedRow(), 1).toString());
            fechaVencimiento.setText(listadeTarjetas.getValueAt(listadeTarjetas.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_editarTarjetaActionPerformed

    private void modificarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarTarjetaActionPerformed
        if (nombreTarjeta.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe agregar el nombre del propietario de la tarjeta", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        if (numTarjeta.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe agregar el número de la tarjeta", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        if (fechaVencimiento.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe agregar la fecha de vencimiento", "Error", JOptionPane.ERROR_MESSAGE);
            this.setVisible(true);
            return;
        }
        Tarjeta tarjetaSeleccionada = listaTarjetas.get(filaSeleccionadaTarjetas);
        //Colocar la nuevz tarjeta en la tabla y en el ArrayList.
        tarjetaSeleccionada.setNombredeTarjeta(nombreTarjeta.getText().trim());
        tarjetaSeleccionada.setNúmerodeTarjeta(numTarjeta.getText().trim());
        tarjetaSeleccionada.setFechadeVencimiento(fechaVencimiento.getText().trim());
        //Actualizar los datos en la tabla.
        String numTarjetaCompleto = numTarjeta.getText();
        String numTarjetaOculto = "**************" + numTarjetaCompleto.substring(14);
        listadeTarjetas.setValueAt(nombreTarjeta.getText().trim(), filaSeleccionadaTarjetas, 0);
        listadeTarjetas.setValueAt(numTarjetaOculto, filaSeleccionadaTarjetas, 1);
        listadeTarjetas.setValueAt(fechaVencimiento.getText().trim(), filaSeleccionadaTarjetas, 2);
        JOptionPane.showMessageDialog(null, "Tarjeta modificada correctamente", "Proceso exitoso", JOptionPane.INFORMATION_MESSAGE);
        //Limpiando los TextFields.
        nombreTarjeta.setText("");
        numTarjeta.setText("");
        fechaVencimiento.setText("");
    }//GEN-LAST:event_modificarTarjetaActionPerformed

    private void eliminarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarTarjetaActionPerformed
        int selectedRow = listadeTarjetas.getSelectedRow();
        //Validando que la fila si haya sido seleccionado.
        if (selectedRow != -1) {
            //Obteniendo el objeto correspondiente al Arraylist dependiendo de la fila seleccionada.
            Tarjeta Tjt = listaTarjetas.get(selectedRow);

            //Método para eliminar la fila seleccionada.
            listaTarjetas.remove(Tjt);

            //Actualizando la tabla sin el objeto que se eliminó previamente.
            DefaultTableModel modelo = (DefaultTableModel) listadeTarjetas.getModel();
            model.removeRow(selectedRow);

            JOptionPane.showMessageDialog(null, "Tarjeta eliminada", "Proceso Exitoso", JOptionPane.INFORMATION_MESSAGE);
            //Vaciando de nuevo los TextFields.
            nombreTarjeta.setText("");
            numTarjeta.setText("");
            fechaVencimiento.setText("");
        } else { //Si no se seleccionó ninguna fila mostrará un JOptionPane con el error.
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila para eliminar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_eliminarTarjetaActionPerformed

